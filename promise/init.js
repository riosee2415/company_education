const condition = true; // ture => resolve,   false => reject

const promise = new Promise((resolve, reject) => {
  if (condition) {
    resolve("resolve");
  } else {
    reject("reject");
  }
});

// if you want to write other code

promise
  .then((message) => {
    console.log(message); // resolve condition
  })
  .catch((error) => {
    console.error(error); // reject condition
  })
  .finally(() => {
    console.log("finally!");
  });

